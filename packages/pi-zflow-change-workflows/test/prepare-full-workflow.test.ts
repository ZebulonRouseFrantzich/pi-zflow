/**
 * Create a temporary directory initialized as a git repo so that runtime
 * state path resolution works correctly (git-dir based).
 */
async function createTestRepo(): Promise<string> {
  const tmpDir = await fs.mkdtemp(path.join(os.tmpdir(), "zflow-test-full-"))
  execFileSync("git", ["init"], { cwd: tmpDir, encoding: "utf-8", stdio: "pipe" })
  execFileSync("git", ["config", "user.email", "test@test.com"], { cwd: tmpDir, encoding: "utf-8", stdio: "pipe" })
  execFileSync("git", ["config", "user.name", "Test User"], { cwd: tmpDir, encoding: "utf-8", stdio: "pipe" })
  // Create an initial commit so git rev-parse HEAD works
  await fs.writeFile(path.join(tmpDir, "README.md"), "# Test", "utf-8")
  execFileSync("git", ["add", "."], { cwd: tmpDir, encoding: "utf-8", stdio: "pipe" })
  execFileSync("git", ["commit", "-m", "Initial commit"], { cwd: tmpDir, encoding: "utf-8", stdio: "pipe" })
  return tmpDir
}

/**
 * Clean up a temporary test repo.
 */
async function removeTestRepo(repoRoot: string): Promise<void> {
  await fs.rm(repoRoot, { recursive: true, force: true })
}

/**
 * Write placeholder plan artifacts so validation passes.
 */
async function writeValidPlanArtifacts(versionDir: string): Promise<void> {
  await fs.mkdir(versionDir, { recursive: true })
  await fs.writeFile(path.join(versionDir, "design.md"), "# Design\n\nReal design content", "utf-8")
  await fs.writeFile(path.join(versionDir, "execution-groups.md"), "# Execution Groups\n\nReal execution groups", "utf-8")
  await fs.writeFile(path.join(versionDir, "standards.md"), "# Standards\n\nReal standards content", "utf-8")
  await fs.writeFile(path.join(versionDir, "verification.md"), "# Verification\n\nReal verification plan", "utf-8")
}

