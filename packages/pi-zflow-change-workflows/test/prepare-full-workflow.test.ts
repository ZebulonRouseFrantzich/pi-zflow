describe("runChangeImplementWorkflow", () => {
  test("creates proper run.json and returns status executing", async () => {
    const repoRoot = await createTestRepo()
    try {
      // First create and approve a plan
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-impl",
      })
      await approvePlanVersion("test-impl", "v1", repoRoot)

      // Now run implementation
      const result = await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-impl",
      })

      assert.strictEqual(result.changeId, "test-impl")
      assert.strictEqual(result.status, "executing", "status should be 'executing', not 'completed'")
      assert.ok(result.runId, "runId should be set")
      assert.ok(result.runId.startsWith("impl-"), "runId should start with 'impl-'")
      assert.strictEqual(result.planVersion, "v1")

      // Verify run.json exists
      const runStatePath = resolveRunStatePath(result.runId, repoRoot)
      const runContent = await fs.readFile(runStatePath, "utf-8")
      const runJson = JSON.parse(runContent)
      assert.strictEqual(runJson.runId, result.runId)
      assert.strictEqual(runJson.changeId, "test-impl")
      assert.strictEqual(runJson.planVersion, "v1")
      assert.strictEqual(runJson.phase, "executing")
      assert.ok(runJson.createdAt, "createdAt should be set")
      assert.ok(runJson.repoRoot, "repoRoot should be set")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("rejects missing approved plan", async () => {
    const repoRoot = await createTestRepo()
    try {
      // Create plan but don't approve it
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-no-approve",
      })

      await assert.rejects(
        async () => {
          await runChangeImplementWorkflow({
            cwd: repoRoot,
            changeId: "test-no-approve",
          })
        },
        {
          message: /No approved plan/,
        },
      )
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("rejects when no plan exists", async () => {
    const repoRoot = await createTestRepo()
    try {
      await assert.rejects(
        async () => {
          await runChangeImplementWorkflow({
            cwd: repoRoot,
            changeId: "no-plan-exists",
          })
        },
        {
          message: /No plan found/,
        },
      )
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("detects dirty worktree", async () => {
    const repoRoot = await createTestRepo()
    try {
      // Create and approve plan
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-dirty",
      })
      await approvePlanVersion("test-dirty", "v1", repoRoot)

      // Create uncommitted change
      await fs.writeFile(path.join(repoRoot, "untracked.txt"), "dirty", "utf-8")

      // Without --force, dirty tree must throw
      await assert.rejects(
        () => runChangeImplementWorkflow({
          cwd: repoRoot,
          changeId: "test-dirty",
        }),
        { message: /Primary worktree must be clean/ },
      )

      // With --force, dirty tree is allowed
      const result = await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-dirty",
        force: true,
      })
      assert.strictEqual(result.status, "executing")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("detects plan state advancing to executing", async () => {
    const repoRoot = await createTestRepo()
    try {
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-state-change",
      })
      await approvePlanVersion("test-state-change", "v1", repoRoot)

      await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-state-change",
      })

      const planStatePath = resolvePlanStatePath("test-state-change", repoRoot)
      const planState = JSON.parse(await fs.readFile(planStatePath, "utf-8"))
      assert.strictEqual(planState.lifecycleState, "executing")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("writes nextSteps array into run.json", async () => {
    const repoRoot = await createTestRepo()
    try {
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-next-steps",
      })
      await approvePlanVersion("test-next-steps", "v1", repoRoot)

      const result = await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-next-steps",
      })

      // Verify result includes nextSteps
      assert.ok(Array.isArray(result.nextSteps), "result.nextSteps should be an array")
      assert.ok(result.nextSteps.length > 0, "result.nextSteps should have entries")

      // Verify run.json contains nextSteps
      const runStatePath = resolveRunStatePath(result.runId, repoRoot)
      const runJson = JSON.parse(await fs.readFile(runStatePath, "utf-8"))
      assert.ok(Array.isArray(runJson.nextSteps), "run.json nextSteps should be an array")
      assert.strictEqual(runJson.nextSteps.length, result.nextSteps.length)
      assert.ok(runJson.nextSteps[0].includes("Context-builder"), "first step should mention context-builder")
      assert.ok(runJson.nextSteps[1].includes("Worktree dispatch"), "second step should mention worktree dispatch")
      assert.ok(runJson.nextSteps[2].includes("Worker verification"), "third step should mention worker verification")
      assert.ok(runJson.nextSteps[3].includes("Apply-back"), "fourth step should mention apply-back")
      assert.ok(runJson.nextSteps[4].includes("Final verification"), "fifth step should mention final verification")
      assert.ok(runJson.nextSteps[5].includes("Code review"), "sixth step should mention code review")
      assert.ok(runJson.nextSteps[6].includes("Fix loop"), "seventh step should mention fix loop")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("registers unfinished run in state-index change lifecycle", async () => {
    const repoRoot = await createTestRepo()
    try {
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-unfinished-lifecycle",
      })
      await approvePlanVersion("test-unfinished-lifecycle", "v1", repoRoot)

      const result = await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-unfinished-lifecycle",
      })

      // Verify change lifecycle has the run in unfinishedRuns
      const index = await loadStateIndex(repoRoot)
      const lifecycle = index.changes["test-unfinished-lifecycle"]
      assert.ok(lifecycle, "change lifecycle should exist")
      assert.strictEqual(lifecycle.lastPhase, "executing")
      assert.ok(Array.isArray(lifecycle.unfinishedRuns), "unfinishedRuns should be an array")
      assert.ok(lifecycle.unfinishedRuns.includes(result.runId), "runId should be in unfinishedRuns")

      // Verify state-index entry exists with status executing
      const runEntry = index.entries.find((e) => e.type === "run" && e.id === result.runId)
      assert.ok(runEntry, "run state-index entry should exist")
      assert.strictEqual(runEntry!.status, "executing")
      assert.strictEqual(runEntry!.metadata?.changeId, "test-unfinished-lifecycle")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("run.json phase is executing with metadata field", async () => {
    const repoRoot = await createTestRepo()
    try {
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-phase-executing",
      })
      await approvePlanVersion("test-phase-executing", "v1", repoRoot)

      const result = await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-phase-executing",
      })

      // Verify run.json phase
      const runStatePath = resolveRunStatePath(result.runId, repoRoot)
      const runJson = JSON.parse(await fs.readFile(runStatePath, "utf-8"))
      assert.strictEqual(runJson.phase, "executing")
      assert.ok(runJson.metadata, "run.json should have metadata field")
      assert.strictEqual(runJson.metadata.worktreeDirty, false)
    } finally {
      await removeTestRepo(repoRoot)
    }
  })
})

