describe("runPlanValidation", () => {
  test("detects missing artifacts", async () => {
    const repoRoot = await createTestRepo()
    try {
      const result = await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-missing-artifacts",
      })

      // Don't write artifacts — they should be missing
      const validation = await runPlanValidation("test-missing-artifacts", "v1", repoRoot)
      assert.strictEqual(validation.pass, false)
      assert.ok(validation.issues.length >= 4, "should have at least 4 issues (one per missing artifact)")
      assert.ok(validation.issues.some((i) => i.includes("design.md")), "should mention missing design.md")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("passes when artifacts exist and have real content", async () => {
    const repoRoot = await createTestRepo()
    try {
      const result = await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-valid-artifacts",
      })

      // Write valid plan artifacts
      const versionDir = path.dirname(result.artifactPaths.design)
      await writeValidPlanArtifacts(versionDir)

      const validation = await runPlanValidation("test-valid-artifacts", "v1", repoRoot)
      assert.strictEqual(validation.pass, true)
      assert.strictEqual(validation.issues.length, 0)
    } finally {
      await removeTestRepo(repoRoot)
    }
  })

  test("detects placeholder markers in artifacts", async () => {
    const repoRoot = await createTestRepo()
    try {
      const result = await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-placeholder-markers",
      })

      // Write artifacts with placeholder markers
      const versionDir = path.dirname(result.artifactPaths.design)
      await fs.mkdir(versionDir, { recursive: true })
      await fs.writeFile(path.join(versionDir, "design.md"), "# Design\n[TODO] fill this in", "utf-8")
      await fs.writeFile(path.join(versionDir, "execution-groups.md"), "# Execution Groups\nawaiting scout dispatch", "utf-8")
      await fs.writeFile(path.join(versionDir, "standards.md"), "# Standards\nTODO: write standards", "utf-8")
      await fs.writeFile(path.join(versionDir, "verification.md"), "# Verification\nReal verification plan", "utf-8")

      const validation = await runPlanValidation("test-placeholder-markers", "v1", repoRoot)
      assert.strictEqual(validation.pass, false)
      assert.ok(validation.issues.length >= 3, "should detect at least 3 placeholder issues")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })
})

