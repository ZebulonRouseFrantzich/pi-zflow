      assert.ok(!repoMapContent.includes("Awaiting repo-mapper"), "repo-map.md should not contain repo-mapper placeholder")
      assert.ok(!repoMapContent.includes("(to be filled by repo-mapper)"), "repo-map.md should not contain repo-mapper filler")
