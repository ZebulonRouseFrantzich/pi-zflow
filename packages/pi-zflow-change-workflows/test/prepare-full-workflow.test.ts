describe("completeWorkflow", () => {
  test("marks plan lifecycle as completed", async () => {
    const repoRoot = await createTestRepo()
    try {
      await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-complete",
      })
      await approvePlanVersion("test-complete", "v1", repoRoot)

      const result = await runChangeImplementWorkflow({
        cwd: repoRoot,
        changeId: "test-complete",
      })

      await completeWorkflow("test-complete", result.runId, repoRoot)

      // Verify plan state
      const planStatePath = resolvePlanStatePath("test-complete", repoRoot)
      const planState = JSON.parse(await fs.readFile(planStatePath, "utf-8"))
      assert.strictEqual(planState.lifecycleState, "completed")

      // Verify run.json phase
      const runStatePath = resolveRunStatePath(result.runId, repoRoot)
      const runJson = JSON.parse(await fs.readFile(runStatePath, "utf-8"))
      assert.strictEqual(runJson.phase, "completed")

      // Verify state-index
      const index = await loadStateIndex(repoRoot)
      const planEntry = index.entries.find(
        (e) => e.type === "plan" && e.metadata?.changeId === "test-complete",
      )
      assert.ok(planEntry, "plan entry should exist")
      assert.strictEqual(planEntry!.status, "completed")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })
})
