      assert.ok(reconContent.includes("Generated by"), "reconnaissance.md should have generation timestamp")
      assert.ok(reconContent.includes("Change path"), "reconnaissance.md should have scope section")
      assert.ok(reconContent.includes("README"), "reconnaissance.md should reference README")
