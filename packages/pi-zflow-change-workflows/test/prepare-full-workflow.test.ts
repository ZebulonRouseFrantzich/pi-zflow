      assert.ok(repoMapContent.includes("Branch"), "repo-map.md should include branch info")
      assert.ok(repoMapContent.includes("HEAD"), "repo-map.md should include HEAD sha")
      assert.ok(repoMapContent.includes("README.md"), "repo-map.md should list top-level files")
