describe("advancePlanLifecycle", () => {
  test("transitions lifecycle state correctly", async () => {
    const repoRoot = await createTestRepo()
    try {
      // Create initial plan state
      const result = await runChangePrepareWorkflow({
        cwd: repoRoot,
        changeId: "test-lifecycle",
      })

      // Advance through states
      await advancePlanLifecycle("test-lifecycle", "validated", repoRoot)
      let planState = JSON.parse(await fs.readFile(result.planStatePath, "utf-8"))
      assert.strictEqual(planState.lifecycleState, "validated")

      await advancePlanLifecycle("test-lifecycle", "reviewed", repoRoot)
      planState = JSON.parse(await fs.readFile(result.planStatePath, "utf-8"))
      assert.strictEqual(planState.lifecycleState, "reviewed")

      await advancePlanLifecycle("test-lifecycle", "approved", repoRoot)
      planState = JSON.parse(await fs.readFile(result.planStatePath, "utf-8"))
      assert.strictEqual(planState.lifecycleState, "approved")

      // Also verify state-index entry was updated
      const index = await loadStateIndex(repoRoot)
      const planEntry = index.entries.find(
        (e) => e.type === "plan" && e.metadata?.changeId === "test-lifecycle",
      )
      assert.ok(planEntry, "plan entry should exist in state index")
      assert.strictEqual(planEntry!.status, "approved")
    } finally {
      await removeTestRepo(repoRoot)
    }
  })
})

